package com.rd.client.common.obj;

import com.google.gwt.user.client.rpc.IsSerializable;


/**
 * 系统参数
 * @author fanglm
 *
 */
public class SysParam implements IsSerializable{
	
	private String CONFIG_CODE;
	
	private String VALUE_INT;
	
	private String VALUE_STRING;
	
	private String SYS_TYPE;

	public String getCONFIG_CODE() {
		return CONFIG_CODE;
	}

	public void setCONFIG_CODE(String cONFIGCODE) {
		CONFIG_CODE = cONFIGCODE;
	}

	public String getVALUE_INT() {
		return VALUE_INT;
	}

	public void setVALUE_INT(String vALUEINT) {
		VALUE_INT = vALUEINT;
	}

	public String getVALUE_STRING() {
		return VALUE_STRING;
	}

	public void setVALUE_STRING(String vALUESTRING) {
		VALUE_STRING = vALUESTRING;
	}

	public String getSYS_TYPE() {
		return SYS_TYPE;
	}

	public void setSYS_TYPE(String sYSTYPE) {
		SYS_TYPE = sYSTYPE;
	}
	
}
